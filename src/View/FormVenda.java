    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblProdutos.getSelectedRow();
        DecimalFormat df = new DecimalFormat("#,###.00");
        
        if (linhaSelecionada >= 0)
        {
            DefaultTableModel tabelaProdutos = (DefaultTableModel)tblProdutos.getModel();
            tabelaProdutos.removeRow(linhaSelecionada);
            
            total = (Double.parseDouble(txtTPagar.getText().replace(".","").replace(",", ".")) - Double.parseDouble(txtValor.getText()));
            txtTPagar.setText(df.format(total));
            
            cbProduto.setSelectedIndex(-1);
            txtQtde.setText("");
            txtValor.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione um produto para remover da lista !");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

