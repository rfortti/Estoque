    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
        // TODO add your handling code here:            
        DefaultTableModel tabelaProdutos = (DefaultTableModel)tblProdutos.getModel();
        Pedido ped = new Pedido();    
        DecimalFormat df = new DecimalFormat("#,###.00");
        
        if (cbFuncionario.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(null, "Selecione um funcionario !","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }
        
        if (tabelaProdutos.getRowCount() == 0)
        {
           JOptionPane.showMessageDialog(null, "É necessário adicionar algum produto !","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE); 
        }
                      
        if (rbFiado.isSelected() && cbCliente.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(null, "Selecione um cliente !","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
        }
                        
        if (tabelaProdutos.getRowCount() != 0)// && cbCliente.getSelectedIndex() != -1)
        {
            try
            {     
                 if (txtVRecebido.getText().equals("0") && rbAVista.isSelected())
                {
                    JOptionPane.showMessageDialog(null, "Informe o Valor Recebido !","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);
                    txtVRecebido.requestFocus();
                }
                else
                {
                    for(int x = 0; x < tblProdutos.getRowCount();x++)
                    {              
                        int ped_Item = Integer.parseInt(tblProdutos.getValueAt(x, 0).toString());  
                        ped.setPedItem(ped_Item);
                    
                        String ped_Produto = String.valueOf(tblProdutos.getValueAt(x, 1).toString());                   
                        ped.setPedProduto(ped_Produto);
                    
                        int ped_Qtde = Integer.parseInt(tblProdutos.getValueAt(x, 2).toString());  
                        ped.setPedQtde(ped_Qtde);
                    
                        float ped_Vtotal = Float.parseFloat(tblProdutos.getValueAt(x, 3).toString().replace(".","").replace(",", "."));  
                        ped.setPedVtotal(ped_Vtotal);
                    
                        String ped_Cliente = "";
                    
                        if (rbAVista.isSelected())
                        {
                            String ped_Pgto = "à Vista";  
                            ped.setPedPgto(ped_Pgto);
                        }
                        else if (rbFiado.isSelected())
                        {
                            String ped_Pgto = "Fiado";  
                            ped.setPedPgto(ped_Pgto);
                            ped_Cliente = cbCliente.getSelectedItem().toString();
                            ped.setPedCliente(ped_Cliente);
                        }                    
                    
                        String ped_Data = lblData.getText();
                        ped.setPedData(ConverteData.converteData(ped_Data));
                    
                        if (rbAVista.isSelected())
                        {
                            String ped_Situacao = "P A G O";
                            ped.setPedSituacao(ped_Situacao);
                        }
                        else if (rbFiado.isSelected())
                        {
                            String ped_Situacao = "Em Aberto";
                            ped.setPedSituacao(ped_Situacao);
                        }
                    
                        String ped_Func = cbFuncionario.getSelectedItem().toString();  
                        ped.setPedFunc(ped_Func);
                    
                        if(this.pedidoDAO.inserir(ped) == true)
                        {
                            JOptionPane.showMessageDialog(null, "Pedido do(a) cliente [ "+cbCliente.getSelectedItem()+" ] inserido com sucesso ! \n\n"
                                    + "Obrigado e Volte Sempre !!","ATENÇÃO",JOptionPane.INFORMATION_MESSAGE);                    
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "Não foi possível salvar este pedido !","ATENÇÃO",JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    Limpar();
                 }
            }
            catch(NumberFormatException | HeadlessException e)
            {
                JOptionPane.showMessageDialog(null, "Não foi possível salvar este pedido \n\n Contate o Administrador do Sistema ! !\nErro: " + e.getMessage(),"ATENÇÃO",JOptionPane.ERROR_MESSAGE);
                System.out.println(e.getMessage());
                Limpar();
            }
        }
    }//GEN-LAST:event_btnConcluirActionPerformed

