    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        Tipo t = new Tipo();
        t.setCodTipo(Integer.parseInt(txtCod.getText()));

        int dialogButton = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o tipo selecionada ?", "Warning", 0);// 0 = botão Sim e Não

        if (dialogButton == JOptionPane.YES_OPTION) {
            if (this.tipoDAO.excluir(t) == true) {
                JOptionPane.showMessageDialog(null, "Tipo excluido!");
                Limpar();
                Desabilitar();
                btnNovo.setEnabled(true);
                btnAlterar.setEnabled(false);
                btnExcluir.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnSalvar.setEnabled(false);

                preencheTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao excluir tipo!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

