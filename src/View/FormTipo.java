    private void rbCodigoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rbCodigoMouseClicked
        // TODO add your handling code here:
        if (rbCodigo.isSelected()) {
            ArrayList<Tipo> tipo = new ArrayList<Tipo>();
            tipo = this.tipoDAO.getTiposByCod();
            
            DefaultTableModel tabela = (DefaultTableModel) tblTipo.getModel();
            tabela.setNumRows(0);          
            
            for (Tipo t : tipo) 
            {
                if (t != null) 
                {
                    Object[] obj = new Object[]{
                        t.getCodTipo(),
                        t.getDescTipo(),
                        t.getSiglaTipo()
                    };
                    tabela.addRow(obj);
                }
            }
        }                
    }//GEN-LAST:event_rbCodigoMouseClicked

